// toggles the sides option on
$("#Sides").click(function () {
    $("#Main-menu").hide();
    $("#Sides-Menu").show();
    $("#Drink-menu").hide();
});
// toggles the Drinks option on
$("#Drinks").click(function () {
    $("#Main-menu").hide();
    $("#Sides-Menu").hide();
    $("#Drink-menu").show();
});

// toggles the pizza option on
$("#pizza-btn").click(function () {
    $("#Main-menu").show();
    $("#Sides-Menu").hide();
    $("#Drink-menu").hide();
    $("#pizza").show();
    $("#burgers").hide();
    $("#Breakfast").hide();
    $("#ChickenBuckets").hide();
    $("#AllstarsCombo").hide();
    $("#Bagettes").hide();
    $("#Natchos").hide();
    $("#Meals").hide();
    $("#HotDogs").hide();

});
